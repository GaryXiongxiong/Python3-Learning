#practicing for turtle library
import turtle
turtle.setup(680,680,200,200)
turtle.penup()
turtle.right(90)
turtle.fd(150)
turtle.pendown()
turtle.pensize(25)
turtle.pencolor("red")
turtle.fillcolor("tomato")
turtle.begin_fill()
turtle.seth(135)
turtle.fd(200)
turtle.circle(-100,180)
turtle.left(90)
turtle.circle(-100,180)
turtle.fd(200)
turtle.end_fill()
turtle.done()